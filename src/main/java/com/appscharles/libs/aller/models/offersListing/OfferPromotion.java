package com.appscharles.libs.aller.models.offersListing;

/**
 * The type Offer promotion.
 */
public class OfferPromotion {

    private boolean emphasized;

    private boolean bold;

    private boolean highlight;

    /**
     * Instantiates a new Offer promotion.
     */
    public OfferPromotion() {
    }

    /**
     * Gets emphasized.
     *
     * @return the emphasized
     */
    public boolean getEmphasized() {
        return emphasized;
    }

    /**
     * Sets emphasized.
     *
     * @param emphasized the emphasized
     */
    public void setEmphasized(boolean emphasized) {
        this.emphasized = emphasized;
    }

    /**
     * Gets bold.
     *
     * @return the bold
     */
    public boolean getBold() {
        return bold;
    }

    /**
     * Sets bold.
     *
     * @param bold the bold
     */
    public void setBold(boolean bold) {
        this.bold = bold;
    }

    /**
     * Gets highlight.
     *
     * @return the highlight
     */
    public boolean getHighlight() {
        return highlight;
    }

    /**
     * Sets highlight.
     *
     * @param highlight the highlight
     */
    public void setHighlight(boolean highlight) {
        this.highlight = highlight;
    }
}
